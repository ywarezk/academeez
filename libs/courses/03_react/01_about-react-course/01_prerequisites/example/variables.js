/**
 * Defining variables in js using - var, let, const
 */

var hello = 'world';
hello = 42;
// number of var assignments => infinite
// scope of var is functional

let foo = 'bar';
foo = true;
// number of foo assignments => infinite
// scope of let is inside the block

const myArray = [];
// myArray = 'hello' //ERROR!
// number of const assignment is 1
// scope of const is in the block


